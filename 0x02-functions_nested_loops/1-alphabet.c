#include "main.h"

/**
 * print_alphabet - Entry point
 *
 * Description: This function prints the alphabets in lowercase
 *
 * Return: Always 0 (success)
 */

void print_alphabet(void)
/*
 * print_alphabet - the for loop is used to run through the alphabets
 * from a to z
 */

{
int x;
for (x = 'a'; x <= 'z'; x++)
{
_putchar(x);
}
{
_putchar('\n');
}
}
